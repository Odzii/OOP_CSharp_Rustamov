using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Model
{
    /// <summary>
    /// Управляет трёхмерными геометрическими фигурами, которые имеют объём,
    /// предоставляя общий интерфейс для получения типа фигуры, 
    /// расчёта объёма и получения текстового описания фигуры.
    /// </summary>
    /// <remarks> Получается, 
    /// что этот абстрактный базовый класс будет служить основой 
    /// для конкретных реализаций различных типов объёмных фигур, 
    /// таких как параллелепипеды, пирамиды и сферы. 
    /// Он определяет контракт для всех производных классов, 
    /// гарантируя, что они предоставляют информацию о типе фигуры, 
    /// её объёме и текстовом описании.</remarks>
    public abstract class VolumeFigureBase : IVolumeFigure
    {
        /// <summary>
        /// Получить тип фигуры, представляемой текущим экземпляром.
        /// </summary>
        /// <remarks>
        /// Это свойство должно быть реализовано в производных классах
        /// для возвращения конкретного типа фигуры,
        /// который они представляют. 
        /// Например, класс, представляющий сферу, 
        /// может возвращать строку "Сфера", 
        /// а класс, представляющий параллелепипед, 
        /// может возвращать "Параллелепипед".
        /// </remarks>
        public abstract string FigureType { get;  }

        /// <summary>
        /// Получить объём объекта, представленного кубической фигурой.
        /// </summary>
        /// <remarks> 
        /// Значение вычисляется производным классом 
        /// на основе конкретной геометрии объекта. 
        /// Реализации должны гарантировать, 
        /// что возвращаемое значение точно отражает объем объекта 
        /// </remarks>
        public abstract double Volume { get; }

        /// <summary>
        /// Возвращает описание текущего объекта, содержащее сведения, 
        /// необходимые для понимания его назначения и способа использования.
        /// </summary>
        /// <returns>
        /// Строка с описанием объекта, которая может включать
        /// его ключевые свойства или особенности поведения.
        /// </returns>
        public abstract string GetDescription();

        /// <summary>
        /// Проверяет, что указанное значение является положительным конечным числом.
        /// </summary>
        /// <param name="value">Проверяемое числовое значение. 
        /// Должно быть больше нуля, не быть NaN и не быть бесконечностью.</param>
        /// <param name="parameterName">Имя проверяемого параметра. 
        /// Используется в сообщениях об исключениях для указания некорректного аргумента.</param>
        /// <exception cref="ArgumentOutOfRangeException">
        /// Выбрасывается, если значение меньше или равно нулю, 
        /// равно NaN или является бесконечностью.
        /// </exception>
        protected static void ValidatePositive(double value, string parameterName)
        {
            if (Double.IsNegative(value))
            {
                throw new ArgumentOutOfRangeException(
                    parameterName,
                    "Значение должно быть больше нуля.");
            }

            if (Double.IsNaN(value))
            {
                throw new ArgumentOutOfRangeException(
                    parameterName,
                    "Значение не должно быть неопределённым");
            }

            if (Double.IsInfinity(value))
            {
                throw new ArgumentOutOfRangeException(
                parameterName,
                "Значение не должно быть бесконечно большим");
            }
        }

    }
}
